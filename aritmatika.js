let x = 10;
let y = 5;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(y % x); //modulus

//operator
console.log(x > y);
console.log(x < y);
console.log(x >= y);
console.log(x <= y);
console.log(x === y); //membandingkan kedua nilai memperhatikan tipe data
console.log(x == y); //membandingkan kedua nilai tapi mengabaikan tipe data
console.log(x !== y); //membandingkan kedua nilai beseta tipe data harus tidak sama
console.log(x != y); //membandingkan kedua nilai harus tidak sama tapi mengabaikan tipe data
//logical operator
console.log(x && y); //and
console.log(x || y); //or
console.log(!x); //not

//increment
let post = 10;
console.log(post++); //expression akan menghasilkan nilai variable sebelum ditingkatkan nilainya
console.log(post); //meghasilkan output 11

//decrement
let pre = 5;
console.log(++pre); //expression akan menghasilkan nilai variable setelah ditingkatkan nilainya
